package getonFast.hj.semi.main.model.vo;

public class Exhibit {
	private int exNo;
	private String exNm;
	private String exSubNm;
	private String exImg;
	
	private void Exhibit() {
		
	}

	public int getExNo() {
		return exNo;
	}

	public void setExNo(int exNo) {
		this.exNo = exNo;
	}

	public String getExNm() {
		return exNm;
	}

	public void setExNm(String exNm) {
		this.exNm = exNm;
	}

	public String getExSubNm() {
		return exSubNm;
	}

	public void setExSubNm(String exSubNm) {
		this.exSubNm = exSubNm;
	}

	public String getExImg() {
		return exImg;
	}

	public void setExImg(String exImg) {
		this.exImg = exImg;
	}
	
	

}
