package getonFast.hj.semi.admin.model.vo;

public class AdminMember {
	private int memberNo;
	private String memberEmail;
	private String memberNm;
	private String memberImgPath;
	private String memberImgNm;
	private int memberSt;
	private String memberRegDt;
	private String memberSecDt;

	public AdminMember() {
	}

	public int getMemberNo() {
		return memberNo;
	}

	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}

	public String getMemberEmail() {
		return memberEmail;
	}

	public void setMemberEmail(String memberEmail) {
		this.memberEmail = memberEmail;
	}

	public String getMemberNm() {
		return memberNm;
	}

	public void setMemberNm(String memberNm) {
		this.memberNm = memberNm;
	}

	public String getMemberImgPath() {
		return memberImgPath;
	}

	public void setMemberImgPath(String memberImgPath) {
		this.memberImgPath = memberImgPath;
	}

	public String getMemberImgNm() {
		return memberImgNm;
	}

	public void setMemberImgNm(String memberImgNm) {
		this.memberImgNm = memberImgNm;
	}

	public int getMemberSt() {
		return memberSt;
	}

	public void setMemberSt(int memberSt) {
		this.memberSt = memberSt;
	}

	public String getMemberRegDt() {
		return memberRegDt;
	}

	public void setMemberRegDt(String memberRegDt) {
		this.memberRegDt = memberRegDt;
	}

	public String getMemberSecDt() {
		return memberSecDt;
	}

	public void setMemberSecDt(String memberSecDt) {
		this.memberSecDt = memberSecDt;
	}

	

}
